export function t(key: string, lang: string = 'English'): string {
  const l = lang === 'Russian' ? 'ru' : 'en'
  return translations[l]?.[key] ?? key
}

export function langName(lang: string): string {
  if (lang === 'Russian') return 'Русский'
  return 'English'
}

export function p(value: string, lang: string): string {
  const l = lang === 'Russian' ? 'ru' : 'en'
  const map: Record<string, Record<string, string>> = {
    en: { Everyone: 'Everyone', 'My Contacts': 'My Contacts', Nobody: 'Nobody' },
    ru: { Everyone: 'Все', 'My Contacts': 'Мои контакты', Nobody: 'Никто' },
  }
  return map[l]?.[value] ?? value
}

export function formatTime(date: Date | string, timeFormat: string = '24h'): string {
  const d = typeof date === 'string' ? new Date(date) : date
  return d.toLocaleTimeString([], {
    hour: '2-digit',
    minute: '2-digit',
    hour12: timeFormat === '12h',
  })
}
